class Sixteen_d{
    public static void main(String[] args){
         int num=352;

         System.out.println("num  into binary String:"+Integer.toBinaryString(num));
         System.out.println("num  into octal String:"+Integer.toOctalString(num));
         System.out.println("num  into hexadecimal String:"+Integer.toHexString(num));
    }
}